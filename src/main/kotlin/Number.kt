fun main() {

    // Number
    var age : Byte = 30
    var height : Int = 40
    var distance : Short = 200
    var balance : Long = 9000000000000L

    println(age)
    println(height)
    println(distance)
    println(balance)

    // floating point number
    var value : Float = 97.86F
    var radius : Double = 123213213123.32432423432

    println(value)
    println(radius)
}